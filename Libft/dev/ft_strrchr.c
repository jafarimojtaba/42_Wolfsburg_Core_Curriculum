char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i])
		i++;
	while (i-- != 0)
	{
		if (str[i] == c)
			return (&str[i]);
	}
	return (0);
}
